#include <stdio.h>

int main() {
    int i,j,k;
    //fb ff ff ff 小端存储
    // -> 1111 1011 1111 1111 1111 1111 1111 1111
    // -> 1111 1111 1111 1111 1111 1111 1111 1011 //按位取反+1得到原码
    // -> -000 0000 0000 0000 0000 0000 0000 0101 -> -5
    i=-5;//1111 1111 1111 1111 1111 1111 1111 1011
    j=2;//0000 0000 0000 0000 0000 0000 0000 0010
    k=i+j;//1111 1111 1111 1111 1111 1111 1111 1101 //i和j直接按位相加，之后按位取反+1
    // -> -000 0000 0000 0000 0000 0000 0000 0011 ->-3
    printf("%d",k);
    return 0;
}

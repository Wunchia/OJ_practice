#include <stdio.h>
//float:1+8+24=32 => 8: 去掉全0，全1 1~254 减127 得-126~127 => 单精度浮点数取值范围：2^-126~2^127
//double:1+11+52=64 => 11: 去掉全0，全1 1~2046 减1023 得-1022~1023 => 双精度浮点数取值范围：2^-1022~2^1023
//===========================================
//40 90 00 00
//0 100 0000 1 001 0000 0000 0000 0000 0000
//+ 129-127=2  1.001*2^2=100.1 -> 4.5
//----------------------------------------------
//3f ba 5e 35
//0 011 1111 1 011 1010 0101 1110 0011 0101
//+ 127-127=0  1.011 1010 0101 1110 0011 0101*2^0=1.011 1010 0101 1110 0011 0101 -> 近似表示1.456
//整数部分：1  小数部分：1/4+1/8+1/16+1/64+……=0.25+0.125+0.0625+0.015625+……=0.453125+……

int main() {
    float i=4.5;//内存视图 00 00 90 40 小端存储
    float j=1.456;//35 5e ba 3f
    printf("%f\n",i);
    printf("%f\n",j);
    //浮点数的精度丢失
    float a=1.23456789e10;//12345678900  实际存储的值：12345678800 发生精度丢失
    float b;
    b=a+20;//12345678920
    printf("b=%f\n",b);//12345678848

    return 0;
}
